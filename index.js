function theBeatlesPlay(musicians, instruments) {
  
  var instrumentsMusiciansPlay = [];
  
  for ( let i = 0; i < musicians.length; i++){

    instrumentsMusiciansPlay.push( musicians[i] + " plays " + instruments[i]);
  }
  
  return instrumentsMusiciansPlay;
}


